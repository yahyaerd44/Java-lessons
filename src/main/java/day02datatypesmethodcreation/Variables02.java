package day02datatypesmethodcreation;

public class Variables02 {
    public static void main(String[] args) {
        //  Ornek 1 : Sehir ismi icin bir variable olusturun ve once "Ankara" sonrada "İzmir" degeri atayio ekrana yazdirin

        String sehirİsmi = "Ankara";
        sehirİsmi ="Izmir";
        System.out.println(sehirİsmi);
        //Ornek 2 :Sehir plaka kodu icin bir variable olusturun ve once "06" sonra da "35" degerini atayip ekrana yazdirin
        byte PlakaKodu= 06;
        System.out.println(PlakaKodu);
        PlakaKodu=35;
        System.out.println(PlakaKodu);

        //Ornek 3 :Basarili mi ? sorusu icin bir variable olusturun ve once "false" sonra da "true" degeri atayip ekrana yazdirin.
        boolean basariliMi= false;
        System.out.println(basariliMi);
        basariliMi= true;
        System.out.println(basariliMi);

    }






}
